package com.canvas;

public class Point {

    //setting variables to floats
    private final float x;
    private final float y;

    //creating the constructor to
    //get its arguments
    public Point(float x, float y){
        this.x = x;
        this.y = y;
    }

    public float getX(){
        return x;
    }

    public float getY(){
        return y;
    }

}
